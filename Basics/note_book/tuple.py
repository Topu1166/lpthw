## Tuple: ############## 
#A tuple is a collection of items that: 
# is ordered 
# is immutable (you can't change it after creation it) 
# that can contain different types (strings, numbers, etc.) 
#Think of it like a list that you can't change 

# Example: 

my_tuple = ("apple", "banana", "cherry") 
print("The type of my_tuple is:", type(my_tuple))
print("my_tuple = {}".format(my_tuple)) 

